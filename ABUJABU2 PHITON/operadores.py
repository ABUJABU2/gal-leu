#OPERAÇÃO DE SOMA EM PHITON
num1 = int(input('digite o numero desejado: '))#transformei uma string(inteiro) em int(numero)
num2 = int(input('digite o segundo numero desejado: '))
soma = num1 + num2
print(f'o resultado da soma é {soma}')

#subtracao
sub = num1 - num2
print(f'o resultado da sub é {sub}')

#multiplicacao

mult = num1 * num2
print(f'o resultado da mult é {mult}')

#divisao

div = num1 / num2
print(f'o resultado da div é {div}')

#operacao de resto

rest = num1 % num2
print(f'o resultado do resto de num1 e num2 é:{rest}')